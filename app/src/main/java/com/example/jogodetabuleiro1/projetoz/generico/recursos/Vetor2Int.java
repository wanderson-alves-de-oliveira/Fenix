public class Vetor2Int {

    int x,y;

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Vetor2Int(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Vetor2Int() {
    }
}


